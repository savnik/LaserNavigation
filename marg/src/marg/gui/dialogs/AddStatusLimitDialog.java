    public AddStatusLimitDialog(java.awt.Frame parent, boolean modal, StatusVariable statVar) {
        super(parent, modal);
        initComponents();
        ValueLimit oldLimit = statVar.getLimit();
        if (oldLimit == null) {
            String varValue = statVar.getModuleVar().getValue();
            initDialogFields(varValue, varValue, statVar.getModuleVar().getVarName());
        } else {
            initDialogFields(String.valueOf(oldLimit.getMinValue()), String.valueOf(oldLimit.getMaxValue()), statVar.getModuleVar().getVarName());
            initComparisonSigns(statVar.getLimit());
        }
    }

    private void initDialogFields(String minText, String maxText, String varName) {
        jTextMin.setText(minText);
        jTextMax.setText(maxText);
        jLabelVar.setText(jLabelVar.getText() + varName);
    }

    private void initComparisonSigns(ValueLimit limit) {
        if (limit.getMinimumComparison() == ValueLimit.LESS_THAN) {
            jComboMinComp.setSelectedItem("<");
        }
        if (limit.getMaximumComparison() == ValueLimit.LESS_THAN) {
            jComboMaxComp.setSelectedItem("<");
        }
    }

    public boolean wasApproved() {
        return wasApproved;
    }

    public ValueLimit getValueLimit() {
        return valueLimit;
    }

