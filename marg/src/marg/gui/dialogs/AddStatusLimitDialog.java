    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String minText = jTextMin.getText();
        String maxText = jTextMax.getText();
        try {
            Double min = Double.parseDouble(minText);
            Double max = Double.parseDouble(maxText);
            if (min > max) {
                throw new NumberFormatException();
            }
            String minCompString = (String) jComboMinComp.getSelectedItem();
            String maxCompString = (String) jComboMaxComp.getSelectedItem();
            int maxComparison;
            int minComparison;
            if (minCompString.equals("≤")) {
                minComparison = ValueLimit.LESS_THAN_EQUALS;
            } else {
                minComparison = ValueLimit.LESS_THAN;
            }
            if (maxCompString.equals("≤")) {
                maxComparison = ValueLimit.LESS_THAN_EQUALS;
            } else {
                maxComparison = ValueLimit.LESS_THAN;
            }
            valueLimit = new ValueLimit(min, minComparison, max, maxComparison);
            wasApproved = true;
            this.setVisible(false);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Min or Max error", "Parse Error", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_jButton1ActionPerformed

